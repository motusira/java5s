public class ArrayAssignmentDemo {
    public static void main(String[] args) {
        // Исходные массивы
        int a1[] = {1, 2, 3, 4, 5};
        int a2[] = {6, 7, 8, 9, 10};

        // Присваивание ссылок
        a1 = a2;

        // Теперь a1 и a2 ссылаются на один и тот же массив
        System.out.println("a1[0] = " + a1[0]); // 6
        System.out.println("a2[0] = " + a2[0]); // 6

        // Изменение через a1 отразится и на a2
        a1[0] = 99;
        System.out.println("После изменения a1[0] = 99:");
        System.out.println("a1[0] = " + a1[0]); // 99
        System.out.println("a2[0] = " + a2[0]); // 99

        // Объяснение:
        // После выполнения a1 = a2; переменная a1 перестаёт ссылаться
        // на свой исходный массив {1,2,3,4,5} и начинает ссылаться
        // на тот же объект в памяти, что и a2 - массив {6,7,8,9,10}.
        // Исходный массив a1 становится недоступным (если нет других ссылок)
        // и будет удалён сборщиком мусора.
    }
}

